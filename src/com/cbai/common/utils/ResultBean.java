package com.cbai.common.utils;

public class ResultBean {
	private String number;
	
    private Integer wan;
    private Integer qian;
    private Integer bai;
    private Integer shi;
    private Integer ge;

    
	public ResultBean() { }
	
	
	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public Integer getWan() {
		return wan;
	}
	public void setWan(Integer wan) {
		this.wan = wan;
	}
	public Integer getQian() {
		return qian;
	}
	public void setQian(Integer qian) {
		this.qian = qian;
	}
	public Integer getBai() {
		return bai;
	}
	public void setBai(Integer bai) {
		this.bai = bai;
	}
	public Integer getShi() {
		return shi;
	}
	public void setShi(Integer shi) {
		this.shi = shi;
	}
	public Integer getGe() {
		return ge;
	}
	public void setGe(Integer ge) {
		this.ge = ge;
	}
    
}
